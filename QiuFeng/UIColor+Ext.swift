import Foundation

extension UIColor {
    class func appBlueColor() -> UIColor! {
        return UIColor(red: 0, green: 183 / 255.0, blue: 238 / 255.0, alpha: 1)
    }
    class func appGreenColor() -> UIColor! {
        return UIColor(red: 59.0 / 255.0, green: 164.0 / 255.0, blue: 0, alpha: 1)
    }
    class func appOrangeColor() -> UIColor! {
        return UIColor(red: 255.0 / 255.0, green: 123.0 / 255.0, blue: 35.0 / 255.0, alpha: 1)
    }
    class func appDarkGrayColor() -> UIColor! {
        return UIColor(red: 65.0 / 255.0, green: 65.0 / 255.0, blue: 65.0 / 255.0, alpha: 1)
    }
}