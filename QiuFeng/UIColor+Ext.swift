import Foundation

extension UIColor {
    class func appBlueColor() -> UIColor! {
        return UIColor(red: 0, green: 183 / 255.0, blue: 238 / 255.0, alpha: 1)
    }
    class func appGreenColor() -> UIColor! {
        return UIColor(red: 90 / 255.0, green: 238 / 255.0, blue: 0, alpha: 1)
    }
    class func appOrangeColor() -> UIColor! {
        return UIColor(red: 255 / 255.0, green: 123 / 255.0, blue: 35 / 255.0, alpha: 1)
    }
    class func appDarkGrayColor() -> UIColor! {
        return UIColor(red: 65 / 255.0, green: 65 / 255.0, blue: 65 / 255.0, alpha: 1)
    }
}